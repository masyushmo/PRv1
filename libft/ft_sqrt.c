#include "libft.h"

int	ft_sqrt(int nb)
{
	int n;

	n = 2;
	if (nb == 1)
		return (1);
	while (n <= nb / 2)
	{
		if (n * n == nb)
			return (n);
		n++;
	}
	return (0);
}
