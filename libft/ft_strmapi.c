#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int		i;
	char				*new;
	size_t				len;

	if (!s)
		return (0);
	i = 0;
	len = ft_strlen(s);
	new = (char*)malloc(sizeof(*new) * (len + 1));
	if (new == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		new[i] = f(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
